#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		len_s1;

	len_s1 = ft_strlen(s1);
	while (*s2)
	{
		s1[len_s1] = *s2;
		len_s1++;
		s2++;
	}
	s1[len_s1] = '\0';
	return (s1);
}
