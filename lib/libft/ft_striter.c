#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		len;

	if (!s || !f)
		return ;
	len = ft_strlen(s);
	i = -1;
	while (++i < len)
		f(&s[i]);
}
